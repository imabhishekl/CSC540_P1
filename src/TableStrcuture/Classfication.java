public class Classfication 
{
    private String classfication_name;
    private String degree_program;
    private int year;
    private int classification_id;

    /**
     * @return the classfication_name
     */
    public String getClassfication_name() {
        return classfication_name;
    }

    /**
     * @param classfication_name the classfication_name to set
     */
    public void setClassfication_name(String classfication_name) {
        this.classfication_name = classfication_name;
    }

    /**
     * @return the degree_program
     */
    public String getDegree_program() {
        return degree_program;
    }

    /**
     * @param degree_program the degree_program to set
     */
    public void setDegree_program(String degree_program) {
        this.degree_program = degree_program;
    }

    /**
     * @return the year
     */
    public int getYear() {
        return year;
    }

    /**
     * @param year the year to set
     */
    public void setYear(int year) {
        this.year = year;
    }

    /**
     * @return the classification_id
     */
    public int getClassification_id() {
        return classification_id;
    }

    /**
     * @param classification_id the classification_id to set
     */
    public void setClassification_id(int classification_id) {
        this.classification_id = classification_id;
    }
    
}
