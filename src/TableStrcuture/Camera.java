package TableStrcuture;


/**
 *
 * @author amrit
 */
public class Camera {
    private String lens;
    private String model;
    private String make;
    private String memory_available;
    private String camera_id;
    private String lib_name;

    /**
     * @return the lens
     */
    public String getLens() {
        return lens;
    }

    /**
     * @param lens the lens to set
     */
    public void setLens(String lens) {
        this.lens = lens;
    }

    /**
     * @return the model
     */
    public String getModel() {
        return model;
    }

    /**
     * @param model the model to set
     */
    public void setModel(String model) {
        this.model = model;
    }

    /**
     * @return the make
     */
    public String getMake() {
        return make;
    }

    /**
     * @param make the make to set
     */
    public void setMake(String make) {
        this.make = make;
    }

    /**
     * @return the memory_available
     */
    public String getMemory_available() {
        return memory_available;
    }

    /**
     * @param memory_available the memory_available to set
     */
    public void setMemory_available(String memory_available) {
        this.memory_available = memory_available;
    }

    /**
     * @return the camera_id
     */
    public String getCamera_id() {
        return camera_id;
    }

    /**
     * @param camera_id the camera_id to set
     */
    public void setCamera_id(String camera_id) {
        this.camera_id = camera_id;
    }

    /**
     * @return the lib_name
     */
    public String getLib_name() {
        return lib_name;
    }

    /**
     * @param lib_name the lib_name to set
     */
    public void setLib_name(String lib_name) {
        this.lib_name = lib_name;
    }
    
}
